// 29.3. trim-trimEnd-trimStart
// trim()
let s11 = "    He   llo  word     ";
console.log(s11);
console.log(s11.length);
let s12 = s11.trim();
console.log(s12);
console.log(s12.length);
// trimEnd()
let s13 = "    He   llo  word     ";
let s14 = s13.trimEnd();
console.log(s14);
console.log(s14.length);
// trimStart()
let s15 = "    He   llo  word     ";
let s16 = s13.trimStart();
console.log(s16);
console.log(s16.length);