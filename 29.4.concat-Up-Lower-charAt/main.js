// 29.4 concat(), toUpperCase(), toLowerCase(), charAt()
// 5.6 concat: nối chuỗi
let fisrtName = "Jonh";
let lastName = "Doe";
let fullName = fisrtName.concat(" ", lastName);
console.log(fullName);

// 5.7 toUpperCase(): chuyển toàn bộ sang viết hoa
let s18 = "Chào các bạn tôi là AI";
console.log(s18.toUpperCase());

// 5.8 toLowerCase(): chuyển toàn bộ sang viết thường
console.log(s18.toLowerCase());

// 5.9 charAt(index) -> trả về chuỗi nằm ở vị trí index được truyền
let s19 = "Hello";
console.log(s19.charAt(2));

